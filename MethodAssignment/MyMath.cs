namespace MethodAssignment
{
    public class MyMath
    {
        public int MultiplyByTen(int num)
        {
            return num * 10;
        }

        public int DivideByTen(int num)
        {
            return num / 10;
        }

        public int Squared(int num)
        {
            return num * num;
        }
    }
}
